<strong>
Varrodai tevékenység
</strong>
<p>
Szakképzett dolgozóink és gépparkunk együttesen megfelelő hátteret biztosít női és férfi felsőruházat, fürdőruha és egyéb kiegészítők, sportruházat, munkaruházat, autó üléshuzat varrásához, valamint egyéb háztartási és egészségügyi textíliák feldolgozásához.</p>
<p>
Saját szabászattal rendelkezünk, ahol számítógépes gyártás előkészítő rendszer segítségével történik az egyes termékek varrodai előkészítése. Technikai hátterünk sokfélesége lehetővé teszi megrendelőink igényeinek széles körű kielégítését.</p>