<h1>Bemutatkozás</h1>

<p>
Az Egészségügyi Világszervezet (WHO) évente jelenteti meg az International Travel and Health kézikönyvet, mely a fertőző betegségekkel illetve az utazásokkal kapcsolatos kötelezően előírt és ajánlott védőoltásokat területenként részletezi.
</p>
<img src="images/4.jpg" style="float:left; margin-right: 5px;">
<p>
Munkánkat ennek, illetve a CDC (Centers for Diseases Control and Prevention) és az Országos Epidemiológiai Központ szakmai javaslatai alapján, az előírt jogszabályok figyelembevételével végezzük.

Orvosi rendelőnkben – a legmagasabb szakmai színvonalon – az utazásokkal kapcsolatos kötelező illetve ajánlott védőoltásokon kívül több betegség megelőzését szolgáló védőoltás is megkapható.
</p>
<div style="clear:left;"></div>
<p>
A védőoltások mellett szakszerű, személyre szóló és az aktuális utazással kapcsolatos tanácsadás is történik. Ügyfeleinket előre egyeztetett időpontban fogadjuk a felesleges várakozás elkerülése érdekében.
</p>